    private void showCoursesInCombobox() {
        try {
            this.courses = new BUS.BUS_Course().getAll();
            for (DTO_Course course : this.courses) {
                cbSelectCourse.addItem(course.getTITLE());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void showCurrentStudentTable() {
        int selectedCourseId = getSelectedCourseId();
        tbCurrentStudents.removeAll();
        String[] columnNames = {"Student ID", "Last name", "First name"};
        this.currentStudents = new BUS_StudentGrade().getRegistedStudents(selectedCourseId);
        Object[][] data = new Object[this.currentStudents.size()][columnNames.length];
        for (int i = 0; i < this.currentStudents.size(); i++) {
            DTO_Person person = (DTO_Person) this.currentStudents.get(i);
            data[i][0] = person.getId();
            data[i][1] = person.getLast_name();
            data[i][2] = person.getFirst_name();
        }

        DefaultTableModel model = new DefaultTableModel(data, columnNames);
        tbCurrentStudents.setModel(model);
    }

    private void showOrtherStudentTable() {
        int selectedCourseId = getSelectedCourseId();
        tbOrtherStudents.removeAll();
        String[] columnNames = {"Student ID", "Last name", "First name"};
        this.ortherStudents = new BUS_StudentGrade().getUnregistedStudents(selectedCourseId);
        Object[][] data = new Object[this.ortherStudents.size()][columnNames.length];
        for (int i = 0; i < this.ortherStudents.size(); i++) {
            DTO_Person person = (DTO_Person) this.ortherStudents.get(i);
            data[i][0] = person.getId();
            data[i][1] = person.getLast_name();
            data[i][2] = person.getFirst_name();
        }

        DefaultTableModel model = new DefaultTableModel(data, columnNames);
        tbOrtherStudents.setModel(model);
    }

    private int getSelectedCourseId() {
        if (!this.courses.isEmpty()) {
            return this.courses.get(cbSelectCourse.getSelectedIndex()).getCOURSEID();
        }
        return 0;
    }

    private DTO_StudentGrade getSelectedCurrentStudent() {
        return this.currentStudents.get(tbCurrentStudents.getSelectedRow());
    }

    private DTO_Person getSelectedOrtherStudent() {
        return this.ortherStudents.get(tbOrtherStudents.getSelectedRow());
    }

