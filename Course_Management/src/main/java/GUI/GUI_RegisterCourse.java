    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (tbOrtherStudents.getSelectedRow() > -1) {
            DTO_Person studentGrade = getSelectedOrtherStudent();
            int selectedCourseId = getSelectedCourseId();
            try {
                new BUS_StudentGrade().add(selectedCourseId, studentGrade.getId());
                showCurrentStudentTable();
                showOrtherStudentTable();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        if (tbCurrentStudents.getSelectedRow() > -1) {
            DTO_StudentGrade studentGrade = getSelectedCurrentStudent();
            int selectedCourseId = getSelectedCourseId();
            try {
                new BUS_StudentGrade().delete(studentGrade.getENROLLMENTID(), selectedCourseId);
                showCurrentStudentTable();
                showOrtherStudentTable();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

