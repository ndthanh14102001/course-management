    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        try {
            int row = tbInstructors.getSelectedRow();
            String id = tbInstructors.getModel().getValueAt(row, 0).toString();
            BUS_Person personbll = new BUS_Person();
            personbll.Delete(id);
            showInstructors();
        } catch (Exception e) {
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame, "Delete instructor error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

