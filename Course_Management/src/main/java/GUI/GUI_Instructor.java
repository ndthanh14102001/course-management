    void showInstructors() {
        tbInstructors.removeAll();
        BUS_Person personbll = new BUS_Person();
        persons = personbll.GetAllInstructorsWithCourseCount();
        tbInstructors.setModel(convertPersons(persons));
    }

    private DefaultTableModel convertPersons(List list) {
        String[] columnNames = {"ID", "Last Name", "First Name", "Hire Date", "Instructing Courses"};
        Object[][] data = new Object[list.size()][5];
        for (int i = 0; i < list.size(); i++) {
            DTO_Person persons = (DTO_Person) list.get(i);
            data[i][0] = persons.getId();
            data[i][1] = persons.getLast_name();
            data[i][2] = persons.getFirst_name();
            data[i][3] = persons.getHireDateString();
            data[i][4] = persons.getCourseCount();
        }
        DefaultTableModel model = new DefaultTableModel(data, columnNames);
        return model;
    }

