    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String title = txtTitle.getText();
        String url = txtUrl.getText();
        String credits = txtCredits.getText();
        int departmentId = this.departments.get(cbDepartment.getSelectedIndex()).getID();

        DTO_Course course = new DTO_Course();
        course.setTITLE(title);
        course.setURL(url);
        course.setCREDITS(credits);
        course.setDEPARTMENTID(departmentId);


        try {
            new BUS_Course().addOnlineCourse(course);
            JFrame frame = new JFrame("JOptionPane showMessageDialog example");
            JOptionPane.showMessageDialog(frame,
                    "Add online course successfully",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);
            txtTitle.setText("");
            txtCredits.setText("");
            txtUrl.setText("");
            txtCredits.setText("");
        } catch (Exception e) {
            e.printStackTrace();
            JFrame frame = new JFrame("JOptionPane showMessageDialog example");
            JOptionPane.showMessageDialog(frame,
                    "Add online course fail",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

