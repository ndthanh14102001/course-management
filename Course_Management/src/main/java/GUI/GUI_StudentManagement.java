    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int row = tbStudents.getSelectedRow();
            String id = tbStudents.getModel().getValueAt(row, 0).toString();
            BUS_Person personbll = new BUS_Person();
            personbll.Delete(id);
            showStudents();
        } catch (Exception e) {
            JFrame frame = new JFrame();
            JOptionPane.showMessageDialog(frame, "Delete instructor error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

